export function disableMatriculaInput() {
    // Desabilitar o input com a classe .matricula-input
    var matriculaInput = document.getElementById('matricula-input');
    if (matriculaInput) {
        matriculaInput.disabled = true;
        matriculaInput.setAttribute("disabled", "disabled");
    }
}

